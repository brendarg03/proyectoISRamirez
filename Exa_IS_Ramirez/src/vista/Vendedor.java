    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //llamada a la conexión de BD
        conBD.conectarBase();
        
        //capturar los datos desde el formulario a el objeto
        objEquipo.setIdEquipo(Integer.parseInt(txtRegistroID.getText()));
        objEquipo.setMarca(txtRegistroMarca.getText());
        objEquipo.setModelo(txtRegistroModelo.getText());
        objEquipo.setPrecio(Integer.parseInt(txtRegistroPrecio.getText()));

        //insertar un registro hasta base de datos
        String altaEquipo = "INSERT INTO t_equipo values ('"+objEquipo.getIdEquipo()+"', '"+objEquipo.getMarca()+"', '"+objEquipo.getModelo()+"', '"+objEquipo.getPrecio()+"')";
        
        try { //trata de realizar y compilar ejecutar
            conBD.stmt.executeUpdate(altaEquipo);
            JOptionPane.showMessageDialog(null,"Exito al ingresar modelo: \n" + objEquipo.getModelo());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error 1 de BD ALTA\n"+ e);
        }
        
        txtRegistroID.setText(null);
        txtRegistroMarca.setText(null);
        txtRegistroModelo.setText(null);
        txtRegistroPrecio.setText(null);
        
    }//GEN-LAST:event_jButton1ActionPerformed

