    private void btnConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaActionPerformed
        //codigo buscar
        try {//estructura de control de tratamiento de errores
            conBD.conectarBase(); //llamada a la conexion de base de datos
            
            int idEquipoBuscar = Integer.parseInt(this.txtConsultaID.getText());
            
            //instruccion para buscar usuario
            conBD.rs = conBD.stmt.executeQuery("SELECT * FROM t_equipo where idEquipo='"+ idEquipoBuscar +"'");
            JOptionPane.showMessageDialog(null, "Buscando Equipo...");
            if (conBD.rs.next()==true) {
                this.txtConsultaID.setText(conBD.rs.getString("idEquipo"));
                this.txtConsultaMarca.setText(conBD.rs.getString("marca"));
                this.txtConsultaModelo.setText(conBD.rs.getString("modelo"));
                this.txtConsultaPrecio.setText(conBD.rs.getString("precio"));
            } else {
                 JOptionPane.showMessageDialog(null, "Error no existe ese ID de Equipo\nVerifica");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error de BD en busqueda de Usuario"+ e);
        }
    }//GEN-LAST:event_btnConsultaActionPerformed

