    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        conBD.conectarBase();//llamada  a la conexion a BD
        int confirmaBaja;//variable bandera 1 si elimino y 0 no elimno
        
        try {          
            int productoBaja = Integer.parseInt(txtIdProducto.getText());
            confirmaBaja = conBD.stmt.executeUpdate("DELETE FROM t_productos WHERE idProducto = '"+ productoBaja +"'");
            
            if (confirmaBaja == 1) {//si elimino
                JOptionPane.showMessageDialog(null,"Se dio de baja el Id de Producto: \n" + productoBaja);
            } else {
                JOptionPane.showMessageDialog(null,"Error no existe ese ID de Producto  : \nVerifica  c√≥digo");
            }
            
        } catch (Exception e) {
        JOptionPane.showMessageDialog(null,"Error de BD de baja o eliminar verifica\n"+ e);
        }
        
        //limpiar campos 
        txtIdProducto.setText("");
    }//GEN-LAST:event_btnIngresarActionPerformed

